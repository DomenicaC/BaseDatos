    public void llenarDatos() {

        Set<Direccion> lista = contDir.printDirCedPer(txtCed.getText());
        for (Direccion dir : lista) {

            Object[] datos = {dir.getCodigo(),
                dir.getCallePrin(),
                dir.getCalleSec(),
                dir.getNumero()
            };
            modelo.addRow(datos);
        }

    }

