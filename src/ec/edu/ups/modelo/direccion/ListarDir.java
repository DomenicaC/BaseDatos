    public void llenarDatos() {

         modelo = (DefaultTableModel) tblDir.getModel();
        Set<Direccion> lista = contDir.printDir();
        for (Direccion dir : lista) {

            Object[] datos = {dir.getCodigo(),
                dir.getCallePrin(),
                dir.getCalleSec(),
                dir.getNumero(),
                dir.getPerCedula()
            };
            modelo.addRow(datos);

        }

    }

