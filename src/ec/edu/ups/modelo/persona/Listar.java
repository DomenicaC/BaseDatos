    public void llenarDatos() {

        DefaultTableModel modelo = (DefaultTableModel) tblPersona.getModel();

        Set<Persona> lista = contPer.printPer();
        for (Persona per : lista) {

            Object[] datos = {per.getCedula(),
                per.getNombre(),
                per.getApellido(),
                per.getEdad(),
                per.getFechaNac(),
                per.getSueldo()
            };
            
            modelo.addRow(datos);
        }

    }

